public class Person {

    String email;
    String cpf;
    String name;
    String birthday;
    Address address;

    public Person(String email, String cpf, String name, String birthday, Address address){
        this.email = email;
        this.cpf = cpf;
        this.name = name;
        this.birthday = birthday;
        this.address = address;
    }

}
